# ── Operator overloading sugar (mirrors Python ``a + b`` etc.) ─────────
# Hexaly's Python wrapper overloads arithmetic and comparison so model
# expressions read naturally. We do the same on `HxExpression` so the MOI
# code can keep using `+`, `<=`, `==`, etc.

# Helpers: looking up the model.
_m(e::HxExpression) = HxModel(e.opt)

Base.:+(a::HxExpression, b::HxExpression) = sum(_m(a), a, b)
Base.:+(a::HxExpression, b::Real) = sum(_m(a), a, b)
Base.:+(a::Real, b::HxExpression) = sum(_m(b), a, b)

Base.:-(a::HxExpression, b::HxExpression) = sub(_m(a), a, b)
Base.:-(a::HxExpression, b::Real) = sub(_m(a), a, b)
Base.:-(a::Real, b::HxExpression) = sub(_m(b), a, b)

Base.:*(a::HxExpression, b::HxExpression) = prod(_m(a), a, b)
Base.:*(a::HxExpression, b::Real) = prod(_m(a), a, b)
Base.:*(a::Real, b::HxExpression) = prod(_m(b), a, b)

Base.:(==)(a::HxExpression, b::Real) = eq(_m(a), a, b)
Base.:(==)(a::Real, b::HxExpression) = eq(_m(b), a, b)
# Note: HxExpression-HxExpression `==` is hash-equality (defined above).
# Use `eq(m, a, b)` explicitly for the modeling-time equality expression.

Base.:<=(a::HxExpression, b::HxExpression) = leq(_m(a), a, b)
Base.:<=(a::HxExpression, b::Real) = leq(_m(a), a, b)
Base.:<=(a::Real, b::HxExpression) = leq(_m(b), a, b)

Base.:>=(a::HxExpression, b::HxExpression) = geq(_m(a), a, b)
Base.:>=(a::HxExpression, b::Real) = geq(_m(a), a, b)
Base.:>=(a::Real, b::HxExpression) = geq(_m(b), a, b)

Base.:<(a::HxExpression, b::HxExpression) = lt(_m(a), a, b)
Base.:<(a::HxExpression, b::Real) = lt(_m(a), a, b)
Base.:<(a::Real, b::HxExpression) = lt(_m(b), a, b)

Base.:>(a::HxExpression, b::HxExpression) = gt(_m(a), a, b)
Base.:>(a::HxExpression, b::Real) = gt(_m(a), a, b)
Base.:>(a::Real, b::HxExpression) = gt(_m(b), a, b)

# Indexed access: `seq[i]` -> `at(model, seq, i)`.
Base.getindex(e::HxExpression, idx::Union{Integer,HxExpression}) =
    at(_m(e), e, idx)
Base.getindex(e::HxExpression, idxs...) = at(_m(e), e, idxs...)
