# Thin Julia layer over the generated `libhexaly.jl` ccalls. Mirrors the
# Python `hexaly.optimizer` API surface so the MOI wrapper code reads
# similarly to the upstream Python examples.
#
# Naming convention:
#   * `HexalyOptimizer`, `HxModel`, `HxExpression`, `HxSolution`, `HxParam`,
#     `HxStatistics`, `HxPhase` — managed Julia wrappers.
#   * Modeling helpers are bare functions taking the model as first arg
#     (e.g. `int!(m, lb, ub)`, `sum(m, args...)`). The bang convention is
#     used for the operations that mutate the model (variable creation,
#     constraint posting); pure expression builders have no bang.

# Extend (rather than shadow) Base's reduction operators — the MOI code uses
# bare `sum`, `prod`, `max`, `min` for non-Hexaly arithmetic, and we add
# `HxModel`-first methods below via `_nary`.
import Base: sum, prod, max, min

# `libhexaly` is set by `deps/deps.jl` (generated by `deps/build.jl`) before
# this file is included. See `src/Hexaly.jl`.

