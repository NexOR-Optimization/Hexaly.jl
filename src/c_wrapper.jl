# ── Managed Julia handles ──────────────────────────────────────────────

mutable struct HexalyOptimizer
    ptr::hxoptimizer
    function HexalyOptimizer()
        ptr = hx_create_optimizer()
        ptr == C_NULL && error("hx_create_optimizer returned NULL")
        opt = new(ptr)
        finalizer(opt) do o
            if o.ptr != C_NULL
                hx_delete_optimizer(o.ptr)
                o.ptr = C_NULL
            end
        end
        return opt
    end
end

# `HxModel` and `HxParam` are convenience handles that share the optimizer's
# lifetime — the underlying C objects are owned by the optimizer and live
# as long as it does.
struct HxModel
    opt::HexalyOptimizer
end

struct HxParam
    ptr::hxattrs
    opt::HexalyOptimizer  # keep optimizer alive
end

struct HxStatistics
    ptr::hxattrs
    opt::HexalyOptimizer
end

struct HxSolution
    ptr::hxsolution
    opt::HexalyOptimizer
end

# Expression / list / array — these are represented by an integer id within
# the model. We keep a `HexalyOptimizer` reference so we can route operations
# back to the right model.
struct HxExpression
    opt::HexalyOptimizer
    id::Cint
end

# Equality/hashing on expression id so we can use HxExpression as dict keys.
Base.:(==)(a::HxExpression, b::HxExpression) = a.opt === b.opt && a.id == b.id
Base.hash(e::HxExpression, h::UInt) = hash((objectid(e.opt), e.id), h)

# ── Accessors mirroring `optimizer.model`, `optimizer.param`, … ───────

model(opt::HexalyOptimizer) = HxModel(opt)
param(opt::HexalyOptimizer) = HxParam(hx_params(opt.ptr), opt)
statistics(opt::HexalyOptimizer) = HxStatistics(hx_stats(opt.ptr), opt)
solution(opt::HexalyOptimizer) = HxSolution(hx_best_solution(opt.ptr), opt)

state(opt::HexalyOptimizer) = hx_state(opt.ptr)
solve!(opt::HexalyOptimizer) = (hx_solve(opt.ptr); nothing)

# ── Expression builders (modeling-time) ───────────────────────────────

create_constant(m::HxModel, v::Integer) =
    HxExpression(m.opt, hx_create_int_constant(m.opt.ptr, Clonglong(v)))
create_constant(m::HxModel, v::AbstractFloat) =
    HxExpression(m.opt, hx_create_double_constant(m.opt.ptr, Cdouble(v)))
create_constant(m::HxModel, v::Real) =
    v isa Integer ? create_constant(m, Int(v)) : create_constant(m, Float64(v))

# Variable factories: `bool()`, `int(lb, ub)`, `float(lb, ub)`.
function bool!(m::HxModel)
    return HxExpression(m.opt, hx_create_expression(m.opt.ptr, HxOp.O_Bool))
end

function int!(m::HxModel, lb::Integer, ub::Integer)
    lo = create_constant(m, Int(lb))
    hi = create_constant(m, Int(ub))
    id = hx_create_expression_2(m.opt.ptr, HxOp.O_Int, lo.id, hi.id)
    return HxExpression(m.opt, id)
end

function float!(m::HxModel, lb::Real, ub::Real)
    lo = create_constant(m, Float64(lb))
    hi = create_constant(m, Float64(ub))
    id = hx_create_expression_2(m.opt.ptr, HxOp.O_Float, lo.id, hi.id)
    return HxExpression(m.opt, id)
end

# n-ary expression with HxExpression operands.
function _nary(m::HxModel, op::Cint, exprs)
    id = hx_create_expression(m.opt.ptr, op)
    for e in exprs
        _add_operand!(m, id, e)
    end
    return HxExpression(m.opt, id)
end

_add_operand!(m::HxModel, expr_id::Cint, operand::HxExpression) =
    hx_expr_add_operand(m.opt.ptr, expr_id, operand.id)
_add_operand!(m::HxModel, expr_id::Cint, c::Integer) =
    _add_operand!(m, expr_id, create_constant(m, Int(c)))
_add_operand!(m::HxModel, expr_id::Cint, c::AbstractFloat) =
    _add_operand!(m, expr_id, create_constant(m, Float64(c)))

# Lift "any" argument (constant or expression) into an HxExpression.
_lift(m::HxModel, e::HxExpression) = e
_lift(m::HxModel, c::Real) = create_constant(m, c)

# Common operators (variadic where it makes sense)
for (name, op) in (
    (:sum, :(HxOp.O_Sum)), (:prod, :(HxOp.O_Prod)),
    (:max, :(HxOp.O_Max)), (:min, :(HxOp.O_Min)),
    (:and_, :(HxOp.O_And)), (:or_, :(HxOp.O_Or)),
)
    @eval $(name)(m::HxModel, args...) =
        _nary(m, $op, (_lift(m, a) for a in args))
end

# Binary operators
for (name, op) in (
    (:eq, :(HxOp.O_Eq)), (:neq, :(HxOp.O_Neq)),
    (:leq, :(HxOp.O_Leq)), (:geq, :(HxOp.O_Geq)),
    (:lt, :(HxOp.O_Lt)), (:gt, :(HxOp.O_Gt)),
    (:sub, :(HxOp.O_Sub)), (:div_, :(HxOp.O_Div)),
    (:mod_, :(HxOp.O_Mod)), (:pow_, :(HxOp.O_Pow)),
    (:dist, :(HxOp.O_Dist)),
)
    @eval function $(name)(m::HxModel, a, b)
        ea, eb = _lift(m, a), _lift(m, b)
        id = hx_create_expression_2(m.opt.ptr, $op, ea.id, eb.id)
        return HxExpression(m.opt, id)
    end
end

# Unary operators
for (name, op) in (
    (:not_, :(HxOp.O_Not)), (:abs_, :(HxOp.O_Abs)),
    (:ceil_, :(HxOp.O_Ceil)), (:floor_, :(HxOp.O_Floor)),
    (:round_, :(HxOp.O_Round)),
)
    @eval function $(name)(m::HxModel, a)
        e = _lift(m, a)
        id = hx_create_expression_1(m.opt.ptr, $op, e.id)
        return HxExpression(m.opt, id)
    end
end

# count(seq) — arity 1 (no second arg) or count(seq, x) for the
# "count occurrences" form.
count_(m::HxModel, seq::HxExpression) =
    HxExpression(m.opt, hx_create_expression_1(m.opt.ptr, HxOp.O_Count, seq.id))

# iif(cond, then, else_)
function iif(m::HxModel, c, t, e)
    ec, et, ee = _lift(m, c), _lift(m, t), _lift(m, e)
    id = hx_create_expression_3(m.opt.ptr, HxOp.O_If, ec.id, et.id, ee.id)
    return HxExpression(m.opt, id)
end

# at(arr, indices...) — indexed access. Hexaly's O_At expression takes the
# array as the first operand and the indices as subsequent operands.
at(m::HxModel, arr::HxExpression, idxs...) = _nary(m, HxOp.O_At, (arr, idxs...))

# `contains(seq, x)` — element-of (used for lists).
contains_(m::HxModel, seq, x) =
    HxExpression(m.opt, hx_create_expression_2(m.opt.ptr,
        HxOp.O_Contains, _lift(m, seq).id, _lift(m, x).id))

# `index_of(seq, x)` — position of x in seq.
index_of(m::HxModel, seq, x) =
    HxExpression(m.opt, hx_create_expression_2(m.opt.ptr,
        HxOp.O_IndexOf, _lift(m, seq).id, _lift(m, x).id))

# list(n) — list decision variable over 0..n-1.
function list!(m::HxModel, n::Integer)
    n_const = create_constant(m, Int(n))
    id = hx_create_expression_1(m.opt.ptr, HxOp.O_List, n_const.id)
    return HxExpression(m.opt, id)
end

# range(lb, ub) — integer range (exclusive upper bound, like Python).
function range_(m::HxModel, lb, ub)
    elb, eub = _lift(m, lb), _lift(m, ub)
    id = hx_create_expression_2(m.opt.ptr, HxOp.O_Range, elb.id, eub.id)
    return HxExpression(m.opt, id)
end

# partition(lists...) — constraint that the lists form a partition.
partition(m::HxModel, lists::AbstractVector{HxExpression}) =
    _nary(m, HxOp.O_Partition, lists)
partition(m::HxModel, lists::HxExpression...) = _nary(m, HxOp.O_Partition, lists)

# distinct(seq) — built-in distinctness on a list.
distinct(m::HxModel, seq::HxExpression) =
    HxExpression(m.opt, hx_create_expression_1(m.opt.ptr, HxOp.O_Distinct, seq.id))

# array(elements...) or array(range, lambda, init)
function array(m::HxModel, elements::AbstractVector)
    exprs = [_lift(m, e) for e in elements]
    return _nary(m, HxOp.O_Array, exprs)
end
array(m::HxModel, args...) = _nary(m, HxOp.O_Array, args)

# lambda_function(f) where `f(args...) -> expression`.
#
# Hexaly's `O_LambdaFunction` is *modeling-time*: we create one `O_Argument`
# expression per parameter, call the user's Julia function with those
# placeholder expressions, and use the returned expression as the lambda
# body. `nargs` is the arity (1 or 2 are the only cases we use in MOI code).
function lambda_function(m::HxModel, f::Function; nargs::Int = 1)
    arg_exprs = HxExpression[
        HxExpression(m.opt, hx_create_expression(m.opt.ptr, HxOp.O_Argument))
        for _ in 1:nargs
    ]
    body = f(arg_exprs...)
    body isa HxExpression || error(
        "lambda_function: closure must return HxExpression; got $(typeof(body))",
    )
    # Hexaly expects `arguments..., body` as the operand order (matching
    # the Python `create_lambda_function`).
    lam_id = hx_create_expression(m.opt.ptr, HxOp.O_LambdaFunction)
    for a in arg_exprs
        hx_expr_add_operand(m.opt.ptr, lam_id, a.id)
    end
    hx_expr_add_operand(m.opt.ptr, lam_id, body.id)
    return HxExpression(m.opt, lam_id)
end

